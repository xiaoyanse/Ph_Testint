package com.entity;

public class Manage { 
	private int id;
	private String mag_no;//管理员编号
	private String mag_name;//管理员姓名
	private String mag_password;//管理员密码
	private String mag_sex;//管理员性别
	private String mag_emial;//管理员邮箱
	private String mag_phone;//管理员电话
	
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMag_no() {
		return mag_no;
	}
	public void setMag_no(String mag_no) {
		this.mag_no = mag_no;
	}
	public String getMag_name() {
		return mag_name;
	}
	public void setMag_name(String mag_name) {
		this.mag_name = mag_name;
	}
	public String getMag_password() {
		return mag_password;
	}
	public void setMag_password(String mag_password) {
		this.mag_password = mag_password;
	}
	public String getMag_sex() {
		return mag_sex;
	}
	public void setMag_sex(String mag_sex) {
		this.mag_sex = mag_sex;
	}
	public String getMag_emial() {
		return mag_emial;
	}
	public void setMag_emial(String mag_emial) {
		this.mag_emial = mag_emial;
	}
	public String getMag_phone() {
		return mag_phone;
	}
	public void setMag_phone(String mag_phone) {
		this.mag_phone = mag_phone;
	}
 
}