package com.entity;

public class Student {
		private int id;
		private String stu_number;//学号
		private String stu_name;//姓名
		private String stu_password;//密码
		private String stu_sex;//性别
		private String stu_phone;//电话号码
		private String stu_email;//邮箱号码
		private String stu_address;//地址
		private String stu_card;//身份证号码
		private String stu_comment;//备注信息
		public Integer getId() {
			return id;
		}
		public void setId(Integer id) {
			this.id = id;
		}
		public String getStu_number() {
			return stu_number;
		}
		public void setStu_number(String stu_number) {
			this.stu_number = stu_number;
		}
		public String getStu_name() {
			return stu_name;
		}
		public void setStu_name(String stu_name) {
			this.stu_name = stu_name;
		}
		public String getStu_password() {
			return stu_password;
		}
		public void setStu_password(String stu_password) {
			this.stu_password = stu_password;
		}
		public String getStu_sex() {
			return stu_sex;
		}
		public void setStu_sex(String stu_sex) {
			this.stu_sex = stu_sex;
		}
		public String getStu_phone() {
			return stu_phone;
		}
		public void setStu_phone(String stu_phone) {
			this.stu_phone = stu_phone;
		}
		public String getStu_email() {
			return stu_email;
		}
		public void setStu_email(String stu_email) {
			this.stu_email = stu_email;
		}
		public String getStu_address() {
			return stu_address;
		}
		public void setStu_address(String stu_address) {
			this.stu_address = stu_address;
		}
		public String getStu_card() {
			return stu_card;
		}
		public void setStu_card(String stu_card) {
			this.stu_card = stu_card;
		}
		public String getStu_comment() {
			return stu_comment;
		}
		public void setStu_comment(String stu_comment) {
			this.stu_comment = stu_comment;
		}
	 
	}